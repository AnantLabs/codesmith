public string GetDefaultCommentHeader(bool isOverwritten)
{
    string header = "";

    header += "/*------------------------------------------------------------------------\r\n";
    header += "<generated>\r\n";
    header += "     This code was generated by The Kinetic Framework v3.0\r\n";
    header += "     Generated at " + DateTime.Now.ToString() + ".\r\n";
    header += "\r\n";
    header += "     The Kinetic Framework is an open source project developed\r\n"; 
    header += "     by Kinetic IG at RCM Technologies (http://www.kineticig.com).\r\n";
    header += "     The latest version of the framework templates and detailed license\r\n";  
    header += "     is available at http://www.codeplex.com/KineticFramework.\r\n";
	header += "\r\n";
	if(isOverwritten) 
	{
		header += "     This file will be overwritten when regenerating your code.\r\n";
	} 
	else 
	{	
		header += "     This file will NOT be overwritten when regenerating your code.\r\n";
	}
    header += "</generated>\r\n";
    header +="------------------------------------------------------------------------*/\r\n";
    
    return header;
}

public string GetDefaultCommentHeader() 
{
	return GetDefaultCommentHeader(true);
}